package edu.ncsu.lubick.localHub.videoPostProduction;

public class VideoEncodingException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = -9108707427447620499L;

	public VideoEncodingException() {
	}

	public VideoEncodingException(String arg0) {
		super(arg0);
	}

	public VideoEncodingException(Throwable arg0) {
		super(arg0);
	}

	public VideoEncodingException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public VideoEncodingException(String arg0, Throwable arg1, boolean arg2,
			boolean arg3) {
		super(arg0, arg1, arg2, arg3);
	}

}
