package edu.ncsu.dlf.localHub.videoPostProduction;

public class MediaEncodingException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = -9108707427447620499L;

	public MediaEncodingException()
	{
	}

	public MediaEncodingException(String arg0)
	{
		super(arg0);
	}

	public MediaEncodingException(Throwable arg0)
	{
		super(arg0);
	}

	public MediaEncodingException(String arg0, Throwable arg1)
	{
		super(arg0, arg1);
	}

	public MediaEncodingException(String arg0, Throwable arg1, boolean arg2,
			boolean arg3)
	{
		super(arg0, arg1, arg2, arg3);
	}

}
