package edu.ncsu.dlf.localHub.videoPostProduction;

public class PostProductionAnimationException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1972229744848101738L;

	public PostProductionAnimationException()
	{
		super();
	}

	public PostProductionAnimationException(String arg0, Throwable arg1)
	{
		super(arg0, arg1);
	}

	public PostProductionAnimationException(String arg0)
	{
		super(arg0);
	}

	public PostProductionAnimationException(Throwable arg0)
	{
		super(arg0);
	}

}
